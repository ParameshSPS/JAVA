package Java_Training.Trainer_Manish.Day_1;

public class Exercise2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        if (a > b && a > c) {
            System.out.println("a is a greater Number: " + a);
        } else if (b > a && b > c) {
            System.out.println("b is greater Number: " + b);
        } else if (c > a && c > b) {
            System.out.println("c is a greater Number: " + c);
        }
    }
}

