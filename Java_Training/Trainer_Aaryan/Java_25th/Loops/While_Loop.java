package Java_Training.Trainer_Aaryan.Java_25th.Loops;

public class While_Loop {
    public static void main(String[] args) {
        int num = 9;
        int i = 1;
        while (i <= 10) {
            // System.out.printf("%d * %d = %d \n", num, i, num * i);
            System.out.println(num + " * " + i + " = " + (num * i));
            i++;
        }
    }
}